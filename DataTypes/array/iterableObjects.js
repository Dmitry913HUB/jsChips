    // –≤—ã–∑–æ–≤–æ–º range[Symbol.iterator](), –∏ –∏–º–µ–Ω–Ω–æ –µ–≥–æ next() –≥–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –∑–Ω–∞—á–µ–Ω–∏—è.
