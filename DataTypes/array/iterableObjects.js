    // –¢–µ—Ö–Ω–∏—á–µ—Å–∫–∏ –º—ã –º–æ–∂–µ–º –æ–±—ä–µ–¥–∏–Ω–∏—Ç—å –∏—Ö –∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Å–∞–º range –∫–∞–∫ –∏—Ç–µ—Ä–∞—Ç–æ—Ä, —á—Ç–æ–±—ã —É–ø—Ä–æ—Å—Ç–∏—Ç—å –∫–æ–¥.

    let range = {
        from: 1,
        to: 5,

        [Symbol.iterator]() {
            this.current = this.from;
            return this;
        },

        next() {
            if (this.current <= this.to) {
                return { done: false, value: this.current++ };
            } else {
                return { done: true };
            }
        }
    };

